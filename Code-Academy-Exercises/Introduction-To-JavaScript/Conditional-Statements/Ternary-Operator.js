let isLocked = false;

isLocked ?
  console.log('You will need a key to open the door.'):

  console.log('You will not need a key to open the door.');


let isCorrect = true;

isCorrect ?
  console.log('Correct!')
:
  console.log('Incorrect!')

let favoritePhrase = 'Love That!';

favoritePhrase === 'Love That!' ?
  console.log('I love that!')
:
console.log("I don't love that!");
//Ternary Operator is used to simplify if else statements. To use this funtion, follow the condition with a "?" and log the conditions and seperate each with a ":". Statements may be deemed true or false.
